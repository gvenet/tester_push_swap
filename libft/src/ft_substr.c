#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sub;

	sub = (char *)ft_memalloc(len + 1);
	if (!(sub) || !s)
		return (NULL);
	if (start < ft_strlen(s))
		return (ft_strncpy(sub, &s[start], len));
	return (sub);
}
