#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*d;
	char	*s;

	d = (char *)dst;
	s = (char *)src;
	if (s < d)
		while (len--)
			d[len] = s[len];
	else
		return (ft_memcpy(d, s, len));
	return (dst);
}
